package model;
/**
 *
 * Model class constructor for login users that holds their respective getters and setters
 *
 */
public class Users {

  public int userID;
  public String userName;
  public String userPassword;

  public Users(){
      this.userID = userID;
      this.userName = userName;
      this.userPassword = userPassword;
  }

public int getUserID(){
      return userID;
}
public String getUserName(){
      return userName;
}
public String getUserPassword(){
      return userPassword;
}


}
